<h1>Alô Alerj</h1>

<p>O Alô Alerj é um canal de comunicação entre você e a Assembleia Legislativa do Estado do Rio de Janeiro (Alerj). Somos uma ouvidoria, de caráter conciliatório e informativo, a serviço do cidadão. O nosso objetivo é ajudar você a solucionar o seu problema. Aqui você pode registrar reclamações, solicitações e sugestões sobre órgãos públicos e/ou privados.</p>

<p>Temos operadores preparados para atender você, de segunda a sexta-feira, das 8h às 19h. Ao ligar para o Alô Alerj tenha em mãos os seus dados para agilizar o atendimento. O registro de sua demanda sempre gera um número de protocolo, que é fornecido por nossos atendentes. Não esqueça de anotá-lo! É por meio desse número que você poderá acompanhar o andamento do seu processo.</p>

<h3>Como Funciona</h3>

<p>Após o cadastro da sua demanda, ela é encaminhada para o órgão competente, que tem, a partir daí, 60 dias para responder. Você receberá em sua casa, por meio de carta, uma cópia do registro da reclamação e também a resposta do órgão competente, se houver. Após o prazo de 60 dias, caso não haja resposta, você pode ligar para o Alô Alerj e demandar que seja enviada uma notificação para a reclamada. Nosso equipe está pronta para auxiliá-lo.</p>

